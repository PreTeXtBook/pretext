    // SECTION 9 — RUNESTONE HOOK INSTALLATION
    // ═══════════════════════════════════════════════════════════════════════
    //
    // Every Runestone interactive component (multiple choice, Parsons, FITB, …)
    // extends RunestoneBase and calls this.logBookEvent(eventData) when the
    // learner submits an answer.
    //
    // We intercept that call by replacing RunestoneBase.prototype.logBookEvent
    // with a wrapper that first calls our recordInteraction() and then delegates
    // to the original method.  This technique is called "monkey-patching" and
    // is safe here because:
    //   - We hold a reference to the original function and always call it.
    //   - We guard against double-patching with the __ptxScormHooked sentinel.
    //   - Any exceptions in our code are caught so Runestone's own logging
    //     is never disrupted.
    //
    // TIMING: The Runestone bundle loads synchronously, so window.RunestoneBase
    // should already be defined when this script runs.  The DOMContentLoaded
    // and runestone:pre-login-complete listeners are fallbacks for edge cases
    // (e.g. deferred or async loading).

    function installHook() {
        // Only patch once; bail if RunestoneBase isn't available yet
        if (!window.RunestoneBase || window.RunestoneBase.__ptxScormHooked) return;

        // Save a reference to the original method so we can chain to it
        var originalLogBookEvent = window.RunestoneBase.prototype.logBookEvent;

        // Replace with our wrapper
        window.RunestoneBase.prototype.logBookEvent = function (eventData) {
            // Our tracking code runs first; any error is caught so it cannot
            // prevent Runestone's own event handling from executing
            try {
                recordInteraction(eventData);
            } catch (err) {
                console.error('[PTX-SCORM] Error in recordInteraction:', err);
            }
            // Always call the original so Runestone continues to function normally
            return originalLogBookEvent.call(this, eventData);
        };

        // Sentinel prevents double-patching if installHook() is called again
        window.RunestoneBase.__ptxScormHooked = true;

        var rsVersion = (window.eBookConfig && window.eBookConfig.runestone_version)
            ? ' (Runestone v' + window.eBookConfig.runestone_version + ')'
            : '';
        console.log('[PTX-SCORM] Runestone hook installed' + rsVersion + '.');
    }

    // Attempt installation immediately (Runestone is usually already loaded)
    installHook();

    // Fallback: Runestone fires this event when its components are ready after
    // the user logs in.  In some configurations RunestoneBase isn't available
    // until this point.
    document.addEventListener('runestone:pre-login-complete', installHook);

    // Final fallback: re-try hook installation after the full DOM is built,
    // count gradeable questions, then initialize SCORM eagerly so we can
    // restore saved answers before Runestone's components call checkServer.
    document.addEventListener('DOMContentLoaded', function () {
        installHook();
        countPageQuestions();
        var restoreMap = loadRestoreData();
        installRestoreHook(restoreMap);
        requestParentResize();
        installWeBWorKSrcdocIntercept();
        installWeBWorKAjaxIntercept();

        // Add status badges to WeBWorK problems that the student has already answered.
        // WeBWorK is not a RunestoneBase component so the restore hook's decorateStatus
        // path never fires for it.  We add a lightweight badge to .problem-buttons so
        // it appears on page load (before Activate), matching the position of the
        // checkmarks Runestone shows for other exercise types.
        Object.keys(_state.answers).forEach(function (divId) {
            var container = document.getElementById(divId);
            if (!container || !container.dataset) return;

            // WeBWorK: data-domain identifies exercise-wrapper elements.
            if (container.dataset.domain) {
                addWeBWorKBadge(container, _state.answers[divId]);
            }

            // DoenetActivity: data-component="doenet" identifies the wrapper div.
            // Doenet resets its credit counter on load (by design), so we add a
            // lightweight badge showing the student's saved score from prior work.
            if (container.dataset.component === 'doenet') {
                addDoenetBadge(container, _state.answers[divId]);
            }
        });
    });


