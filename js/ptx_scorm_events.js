    // SECTION 10 — PARENT FRAME RESIZE REQUEST
    // ═══════════════════════════════════════════════════════════════════════
    //
    // Canvas (and many other LMSes) embed SCORM content in an iframe whose
    // default height is too small for a page full of interactive exercises.
    // Canvas honors the same "lti.frameResize" postMessage that LTI tools and
    // SPLICE activities use to request a taller frame.
    //
    // We send this message at DOMContentLoaded (initial render) and again
    // whenever the page body grows taller — Runestone loads its components
    // asynchronously via webpack dynamic imports, so the page height can
    // increase significantly after the initial paint.
    //
    // If the parent frame is not Canvas (or does not support lti.frameResize),
    // the message is silently ignored — it has no effect on SCORM behavior.

    /**
     * Measure the current page height and ask the parent frame to match it.
     * Called at DOMContentLoaded and again by the ResizeObserver below.
     */
    function requestParentResize() {
        // Don't send if we are already the top-level window (not in an iframe)
        if (window.parent === window) return;

        // scrollHeight reflects the full content height even when the iframe
        // itself is clipped.  We take the larger of the two scroll height
        // properties for cross-browser compatibility, with a 600px floor so
        // the frame is never unreasonably tiny even on a nearly empty page.
        var height = Math.max(
            document.documentElement.scrollHeight,
            document.body ? document.body.scrollHeight : 0,
            600
        );

        var msg = { subject: 'lti.frameResize', height: height };

        // Send to the immediate parent (the SCORM player frame).
        window.parent.postMessage(msg, '*');

        // Also send to the top-level Canvas page.  Canvas's SCORM player
        // (iad.scorm.canvaslms.com) wraps our content in its own iframe, so
        // window.parent is the SCORM player — not Canvas itself.  The outer
        // iframe's height is constrained by CSS on the Canvas page, and the
        // SCORM player does not forward resize requests upward on our behalf.
        // Sending directly to window.top gives Canvas's own JS a chance to
        // intercept the message and resize the outer iframe.  Whether Canvas
        // honours lti.frameResize from SCORM content (vs. LTI tools) is
        // LMS-version-dependent and not guaranteed, but the message is harmless.
        if (window.top !== window.parent) {
            try {
                window.top.postMessage(msg, '*');
            } catch (e) {
                // Blocked by browser security in rare sandboxed-iframe configs
            }
        }
    }

    // Watch the document body for size changes and re-request a resize each
    // time it grows (e.g. as Runestone components finish rendering, or as a
    // student expands a knowl or feedback panel).
    //
    // ResizeObserver is supported by all modern browsers.  If it is somehow
    // unavailable we rely on the single DOMContentLoaded call above, which
    // is better than nothing.
    if (window.ResizeObserver) {
        var _resizeObserver = new ResizeObserver(function () {
            requestParentResize();
        });
        // Start observing once the body exists.  At script-parse time the body
        // is already present (this script runs before </body>), but we guard
        // with a DOMContentLoaded fallback just in case.
        if (document.body) {
            _resizeObserver.observe(document.body);
        } else {
            document.addEventListener('DOMContentLoaded', function () {
                _resizeObserver.observe(document.body);
            });
        }
    }


